package day04_variables_intro;

public class DeclorationTypes {
    public static void main(String[] args){
        int bananas = 34;
        System.out.println(bananas);

        int students = 400;
        System.out.println(students);

        students = 401;
        System.out.println(students);

        int peopleInTheCity, birdsOnTheTree, fishInTheLake;
        peopleInTheCity = 1000;
        birdsOnTheTree = 21;
        fishInTheLake = 100;

        System.out.println(peopleInTheCity);
        System.out.println(birdsOnTheTree);
        System.out.println(fishInTheLake);

        int studentsInNY = 25, studentsInVA = 200;
        System.out.println(studentsInNY);
        System.out.println(studentsInVA);



    }
}
